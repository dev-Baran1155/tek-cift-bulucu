def tek_mi_çift_mi(sayı): #burda fonksiyon işlevini kullandık def ile atadık

    #bu if kısmında ise koşulları kullandık
    
    if sayı % 2 == 0:
        print(f"{sayı} sayısı çift bir sayıdır") 

    else:
        print(f"{sayı} tek bir sayıdır")

tek_mi_çift_mi(38) # burda ise ("bu kısıma giridiniz sayı") buraya bağlı olarak tek mi çift mi oldugunu anlıyor
